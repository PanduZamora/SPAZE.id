function dots4() {
    let dots = document.getElementById("dots4");
    let collapse = document.getElementById("btnCollapse4").classList.contains("collapsed");

    if (collapse == false ) {
        dots.hidden = true;
    } else {
        dots.hidden = false;
    }
}

